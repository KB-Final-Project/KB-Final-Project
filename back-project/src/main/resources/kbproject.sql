                       bmNm VARCHAR(255),                   -- 벤치마크
                       fundCd VARCHAR(100)					-- 펀드코드
);

CREATE TABLE SuikChart (
                           id BIGINT AUTO_INCREMENT PRIMARY KEY, -- 수익 차트 ID (기본 키)
                           fundId BIGINT NOT NULL,                -- 펀드 ID (외래 키)
