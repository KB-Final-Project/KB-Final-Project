CREATE TABLE funds (
                       id BIGINT AUTO_INCREMENT PRIMARY KEY,               -- ID 필드
                       fundFnm VARCHAR(255),               -- 기준가
                       gijunGa DECIMAL(19, 4),            -- 기준일 (기준가는 일반적으로 소수점을 가질 수 있음)
